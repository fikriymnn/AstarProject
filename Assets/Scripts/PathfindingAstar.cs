using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using System;

public class PathfindingAstar : MonoBehaviour
{

    
    GridAstar grid;

    void Awake()
    {
        grid = GetComponent<GridAstar>();
    }

    

    public void FindPath(PathRequestAstar request, Action<PathResultAstar> callback)
    {
        NodeAstar startNode = grid.NodeFromWorldPoint(request.pathStart);
        NodeAstar targetNode = grid.NodeFromWorldPoint(request.pathEnd);

        List<NodeAstar> openSet = new List<NodeAstar>();
        HashSet<NodeAstar> closedSet = new HashSet<NodeAstar>();
        openSet.Add(startNode);

        while (openSet.Count > 0)
        {
            NodeAstar node = openSet[0];
            for (int i = 1; i < openSet.Count; i++)
            {
                if (openSet[i].fCost < node.fCost || openSet[i].fCost == node.fCost)
                {
                    if (openSet[i].hCost < node.hCost)
                        node = openSet[i];
                }
            }

            openSet.Remove(node);
            closedSet.Add(node);

            if (node == targetNode)
            {
                RetracePath(startNode, targetNode);
                return;
            }

            foreach (NodeAstar neighbour in grid.GetNeighbours(node))
            {
                if (!neighbour.walkable || closedSet.Contains(neighbour))
                {
                    continue;
                }

                int newCostToNeighbour = node.gCost + GetDistance(node, neighbour);
                if (newCostToNeighbour < neighbour.gCost || !openSet.Contains(neighbour))
                {
                    neighbour.gCost = newCostToNeighbour;
                    neighbour.hCost = GetDistance(neighbour, targetNode);
                    neighbour.parent = node;

                    if (!openSet.Contains(neighbour))
                        openSet.Add(neighbour);
                }
            }
        }
    }

    Vector3[] RetracePath(NodeAstar startNode, NodeAstar endNode)
    {
        List<NodeAstar> path = new List<NodeAstar>();
        NodeAstar currentNode = endNode;

        while (currentNode != startNode)
        {
            path.Add(currentNode);
            currentNode = currentNode.parent;
        }
        Vector3[] waypoints = SimplifyPath(path);
        Array.Reverse(waypoints);
        return waypoints;

    }

    Vector3[] SimplifyPath(List<NodeAstar> path)
    {
        List<Vector3> waypoints = new List<Vector3>();
        Vector2 directionOld = Vector2.zero;

        for (int i = 1; i < path.Count; i++)
        {
            Vector2 directionNew = new Vector2(path[i - 1].gridX - path[i].gridX, path[i - 1].gridY - path[i].gridY);
            if (directionNew != directionOld)
            {
                waypoints.Add(path[i].worldPosition);
            }
            directionOld = directionNew;
        }
        return waypoints.ToArray();
    }

    int GetDistance(NodeAstar nodeA, NodeAstar nodeB)
    {
        int dstX = Mathf.Abs(nodeA.gridX - nodeB.gridX);
        int dstY = Mathf.Abs(nodeA.gridY - nodeB.gridY);

        if (dstX > dstY)
            return 14 * dstY + 10 * (dstX - dstY);
        return 14 * dstX + 10 * (dstY - dstX);
    }
}